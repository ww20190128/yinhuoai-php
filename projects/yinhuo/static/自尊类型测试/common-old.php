<p>一般来说高自尊优于低自尊，它有利于培养主动、乐观和愉快的感觉。但研究发现，很多社会破坏者的自尊都高于平均水平。当高自尊掺杂了自恋，就会变成大问题，甚至演变成一种暗黑人格特质。高自尊者如果能够学会管理自己的自尊心，将有助于成为更优秀的自己。
</p>
<div class="title2-bg"><span>放下无用的自尊</span></div>
<p>当我们处于脆弱而敏感的状况时，容易执念于那些无足轻重的底线，自尊容易成为要求他人的一件利器。也许，那不过是一种建立在不安全感之上的脆弱的自我陶醉。有人认为“能力和自尊要求是成反比的”，微软联合创始人比尔•盖茨也曾经说过：“这个世界并不在乎你的自尊，人们看到的只是你的成就，在你没有成就以前，切勿过分强调自己的自尊”。所以，高自尊者应放下无用的自尊，放下面具，真实客观的审视自己。
</p>
<div class="title2-bg"><span>学会隐忍和承受</span></div>
<p>懂得隐忍，懂得承受是发力的前提。就像弹簧，压下去，会弹起来更高。就像马云，20世纪90年代时，又矮又瘦的马云挨家挨户推销自己的黄页，他经历的所有的窘迫与无奈，见证了他许下的誓言，他说：再过几年，北京就不会这么对我，再过几年你们都会知道我是干什么的。二十年后，他做到了，这是一个人真正的自尊。懂得隐忍，能成大气。
</p>
<div class="title2-bg"><span>工作中避开自尊心之战</span></div>
