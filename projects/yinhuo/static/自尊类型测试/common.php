<p>高自尊通常优于低自尊，有助于培养积极、乐观的心态。然而，研究表明许多社会破坏者的自尊水平高于平均水平。当高自尊与自恋混合时，可能会产生问题，甚至发展成消极的个性特质。高自尊者如果能够管理好自己的自尊，将有助于成为更优秀的自我。</p>
<div class="title2-bg"><span>放下无用的自尊</span></div>
<p>在我们感到脆弱时，自尊往往变成了一个要求他人的工具，可能只是基于不安全感的自我安慰。比尔·盖茨曾说：“世界不在乎你的自尊，人们关注的是你的成就。在你达成目标之前，不要过分强调自尊。”因此，高自尊者应学会放下不必要的自尊，真实而客观地审视自己。</p>
