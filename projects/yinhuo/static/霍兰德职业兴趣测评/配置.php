<div class="title-wireframe"><span>如何找到自己满意的工作</span></div>
<div class='img-main'><img src="霍兰德-工作.png" title="" alt="" /></div>
<div class="title2-index">
    <div class="index">1</div> <span class="title">列出你意向的工作清单</span>
</div>

<p>拿出一张白纸并对折，在左侧列出你希望长期从事的工作的特点及优势。通过测试，你对此已经有了较全面的了解。在右侧列出不喜欢的工作特点及自身短板。</p>
<p>开始时可以想到什么就写什么，不要受到所学专业的限制。写完初稿后，将职业兴趣、优势、不喜欢的职业特点和劣势简化成5个词以内的关键词，用一张新的对折纸抄写下来，这样你就得到了择业需求清单。</p>

<div class="title2-index">
    <div class="index">2</div> <span class="title">进行初步筛选</span>
</div>

<p>仔细查看择业需求清单，然后拿出一张新的白纸，打开招聘网站，对照其提供的各行业各工种总界面。根据了解和感觉，将与你职业需求清单相符的工作抄写在白纸上，尽可能将让你感觉兴奋和愉快的工作写下来，这样你就得到了初筛工作清单。</p>

<div class="title2-index">
    <div class="index">3</div> <span class="title">确定你职业发展的方向</span></div>

<p>前两步操作主要依靠感觉和喜好，接下来让理性参与进来。根据初筛工作清单，分析找出这些工作的共通点，它们属于什么性质、什么行业？通常可以归纳为3个以内的发展方向。</p>
<p>结合测试结果和对自己职业兴趣及优势的了解，还可以与家人、老师、朋友讨论，选出职业发展方向。这个方向应满足几个条件：</p>
<p>（1）有稳定的市场需求。</p>
<p>（2）能展示你的才华并激发热情。</p>
<p>（3）不违背你的生活方式。</p>

<div class="title2-index">
    <div class="index">4</div> <span class="title">锁定择业范围</span></div>

<p>通过查询求职网站、行业资讯和请教家人、学长等方式，深入了解初筛工作清单上的每个岗位要求和工作特点。进一步了解后，排除不符合长期发展方向和无法协调的工作，缩小择业范围。剩下的工作是符合长期发展方向且易于胜任的。提前做好准备，多尝试应聘，有机会获得面试和试用机会。</p>

<div class="title2-index">
    <div class="index">5</div> <span class="title">学习提升</span></div>

<p>有时我们会选出非常适合的工作，但因欠缺经验或技能而无法完全胜任。这种情况下，如果职业要求是通过学习可以达到的，我们仍然可以尝试。对于应届毕业生而言，二次学习提升是必须的过程。</p>
<p>可以通过查阅专业书籍、搜集网上资料或参加专业机构的培训提升技能。毕业不代表学习的终结，进入职场后也需要不断学习和进步，以提升竞争力。</p>

<div class="title2-index">
    <div class="index">6</div> <span class="title">掌握应聘技巧</span></div>
<p>对于缺乏工作经验的求职者来说，提升相关技能是必要的，但工作经验的欠缺难以避免。面试时应做好两点准备。</p>
<p>（1）尽可能展示与岗位匹配的优势，如能力特质和性格态度。让面试官知道你能为团队做出贡献。面试官更容易接受态度真诚、踏实肯干的新手。</p>
<p>（2）可以降低薪资要求，帮助获得试用机会，从而积累工作经验。不断在岗位上成长后，会得到相应的待遇。</p>
